     private void limparTabela() {
        for (int i = 0; i < listaCarros.size(); i++) {
            tblCarros.setValueAt("", i, 0);
            tblCarros.setValueAt("", i, 1);
            tblCarros.setValueAt("", i, 2);
            tblCarros.setValueAt("", i, 3);
        }
    }
    
